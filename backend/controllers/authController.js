export const loginUser = async (req, res) => {
  const schema = Joi.object({
    email: Joi.string().email().required(),
    password: Joi.string().required(),
  });

  const { error } = schema.validate(req.body);
  if (error) return res.status(400).json({ message: error.details[0].message });

  const { email, password } = req.body;

  try {
    const user = await User.findOne({ email });
    if (!user) return res.status(400).json({ message: "ƒ∞stifad…ô√ßi tapƒ±lmadƒ±" });

    const isMatch = await user.matchPassword(password);
    if (!isMatch) return res.status(400).json({ message: "≈ûifr…ô yalnƒ±≈üdƒ±r" });

    if (!user.isVerified) {
      const verificationToken = crypto.randomBytes(32).toString("hex");
      user.verificationToken = verificationToken;
      user.verificationTokenExpires = new Date(Date.now() + 3600000);
      await user.save();
      await sendVerificationEmail(user.email, verificationToken);
      return res.status(403).json({ message: "Z…ôhm…ôt olmasa emailinizi t…ôsdiql…ôyin" });
    }

    const token = jwt.sign(
      {
        id: user._id,
        username: user.username,
        email: user.email,
        role: user.role,
        firstName: user.firstName,
        lastName: user.lastName,
      },
      process.env.JWT_SECRET,
      { expiresIn: "7d" }
    );

    res.status(200).json({
      message: "Daxil oldunuz",
      token,
      user: {
        id: user._id,
        email: user.email,
        role: user.role,
        firstName: user.firstName,
        lastName: user.lastName,
        username: user.username,
      },
    });
  } catch (err) {
    res.status(500).json({ message: "Server x…ôtasƒ±", error: err.message });
  }
};

