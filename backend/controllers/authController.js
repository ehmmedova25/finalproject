export const registerUser = async (req, res) => {
  const schema = Joi.object({
    firstName: Joi.string().required(),
    lastName: Joi.string().required(),
    username: Joi.string().required(),
    email: Joi.string().email().required(),
    password: Joi.string().min(6).required(),
    confirmPassword: Joi.string().valid(Joi.ref("password")).required(),
  });

  const { error } = schema.validate(req.body);
  if (error) return res.status(400).json({ message: error.details[0].message });

  const { firstName, lastName, username, email, password } = req.body;

  try {
    const existingUser = await User.findOne({ $or: [{ email }, { username }] });
    if (existingUser) {
      return res.status(400).json({ message: "Email v…ô ya istifad…ô√ßi adƒ± artƒ±q m√∂vcuddur." });
    }

    const user = new User({
      firstName,
      lastName,
      username,
      email,
      password,
      role: "user", 
      isVerified: false,
    });

    await user.save();
    res.status(201).json({ message: "Qeydiyyat uƒüurludur. Z…ôhm…ôt olmasa daxil olun." });
  } catch (err) {
    res.status(500).json({ message: "Server x…ôtasƒ±", error: err.message });
  }
};

